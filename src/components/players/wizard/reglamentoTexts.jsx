export const DISCIPLINA_TEXT = `RÉGIMEN INTERNO DISCIPLINARIO

ARTÍCULO 14. DEFINICIÓN
La presente normativa se aplica a todos los Jugadores y Entrenadores del Club.

ARTÍCULO 15. INFRACCIONES

Las faltas se clasifican en leves, graves y muy graves.

Son faltas LEVES:
  • Faltas injustificadas de puntualidad.
  • Faltas injustificadas no reiteradas de asistencia.
  • No comunicar ausencias con antelación suficiente.
  • Actitud pasiva en entrenamientos y partidos.
  • Falta de respeto no grave a compañeros, entrenador, árbitros, etc.
  • Actos de indisciplina, injuria u ofensa no graves.
  • Negarse a participar en un equipo de superior categoría que necesite refuerzos.
  • Reiteración de expulsiones en una temporada.

Son faltas GRAVES:
  • Faltas injustificadas reiteradas de asistencia.
  • Falta de respeto reiterada y continua de carácter grave.
  • Actos de indisciplina, injuria u ofensa grave en deterioro de la imagen del Club.
  • Agresión física grave.
  • Actos que alteren gravemente el normal desarrollo de actividades.
  • 5 faltas leves en una temporada equivalen a 1 falta grave.

Son faltas MUY GRAVES:
  • Actos de indisciplina, injuria u ofensa muy graves.
  • Agresión física muy grave.
  • 3 faltas graves en una temporada equivalen a 1 muy grave.
  • Actitudes xenófobas.
  • Consumo de cualquier tipo de droga.

ARTÍCULO 16. SANCIONES

Por faltas leves: Amonestación verbal o escrita. Suspensión de hasta 7 días.

Por faltas graves: Apercibimiento con informe. Cambio de equipo si es posible. Suspensión de hasta 15 días.

Por faltas muy graves: Pérdida del derecho a la práctica deportiva por más de 30 días, pudiendo llegar a una temporada completa o expulsión indefinida del Club.

ARTÍCULO 17. COMITÉ DE DISCIPLINA DEPORTIVA
Integrado por: el Presidente (o delegado), dos miembros de Junta Directiva, el Director Deportivo y un padre/madre del Club.

ARTÍCULO 18. PROCEDIMIENTO SANCIONADOR
Para faltas graves y muy graves se abrirá expediente con audiencia al jugador, padres, entrenador y Director Deportivo. Plazo máximo de resolución: 1 mes. Las sanciones pueden ser recurridas ante la Junta Directiva.`;